//
static void   expand_tabs   (char* outbuf, char* inbuf) {
    //        ===========
    //
    int col = 0;
    
    for (int c = *inbuf++;
             c;
             c = *inbuf++
    ){
	if (c != '\t') {
	    *outbuf++ = tolower(c);			// libdisasm0 generates upper-case hex constants and I prefer lowercase hex constants, hence the tolower() here.
	    col++;
	} else {
	    do {
		*outbuf++ = ' ';
		col++;
	    } while (col & 7);
	}
    }
    *outbuf++ = '\0';
}

